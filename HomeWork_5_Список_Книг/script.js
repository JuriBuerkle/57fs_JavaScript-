const h1 = document.querySelector('h1'); // –ù–∞—Ö–æ–¥–∏–º –∑–∞–≥–æ–ª–æ–≤–æ–∫
const bookForm = document.getElementById('book-form'); // –ù–∞—Ö–æ–¥–∏–º —Ñ–æ—Ä–º—É –¥–ª—è –¥–æ–±–∞–≤–ª–µ–Ω–∏—è –∫–Ω–∏–≥
const bookList = document.getElementById('book-list'); // –ù–∞—Ö–æ–¥–∏–º —Å–ø–∏—Å–æ–∫ –∫–Ω–∏–≥

h1.addEventListener('click', () => {
    h1.textContent = '–°–ø–∏—Å–æ–∫ –ö–Ω–∏–≥ üìö'; // –ú–µ–Ω—è–µ–º —Ç–µ–∫—Å—Ç –∑–∞–≥–æ–ª–æ–≤–∫–∞ –ø—Ä–∏ –∫–ª–∏–∫–µ
    h1.style.color = 'blue'; // –ú–µ–Ω—è–µ–º —Ü–≤–µ—Ç –∑–∞–≥–æ–ª–æ–≤–∫–∞ –Ω–∞ —Å–∏–Ω–∏–π
    const p = document.createElement('p'); // –°–æ–∑–¥–∞–µ–º –Ω–æ–≤—ã–π –ø–∞—Ä–∞–≥—Ä–∞—Ñ
    p.textContent = '–î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å –≤ –Ω–∞—à—É –ë–∏–±–ª–∏–æ—Ç–µ–∫—É –∫–Ω–∏–≥! –ó–¥–µ—Å—å –≤—ã –Ω–∞–π–¥–µ—Ç–µ –∏–Ω—Ç–µ—Ä–µ—Å–Ω—ã–µ –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏—è –¥–ª—è —á—Ç–µ–Ω–∏—è.';
    p.style.fontSize = '18px'; // –ú–µ–Ω—è–µ–º —Ä–∞–∑–º–µ—Ä —à—Ä–∏—Ñ—Ç–∞ –ø–∞—Ä–∞–≥—Ä–∞—Ñ–∞
    p.style.color = 'green'; // –ú–µ–Ω—è–µ–º —Ü–≤–µ—Ç —Ç–µ–∫—Å—Ç–∞ –ø–∞—Ä–∞–≥—Ä–∞—Ñ–∞ –Ω–∞ –∑–µ–ª–µ–Ω
    document.body.appendChild(p); // –î–æ–±–∞–≤–ª—è–µ–º –ø–∞—Ä–∞–≥—Ä–∞—Ñ –≤ —Ç–µ–ª–æ –¥–æ–∫—É–º–µ–Ω—Ç–∞
});

const books = []; // –ú–∞—Å—Å–∏–≤ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –∫–Ω–∏–≥

// –û–±—Ä–∞–±–æ—Ç—á–∏–∫ —Å–æ–±—ã—Ç–∏—è –¥–ª—è —Ñ–æ—Ä–º—ã –¥–æ–±–∞–≤–ª–µ–Ω–∏—è –∫–Ω–∏–≥–∏
bookForm.addEventListener("submit",(event) => {
    event.preventDefault(); // –û—Ç–º–µ–Ω—è–µ–º —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ–µ –ø–æ–≤–µ–¥–µ–Ω–∏–µ —Ñ–æ—Ä–º—ã
    let task ={
        title: event.target.title.value.toLowerCase(), // –ü–æ–ª—É—á–∞–µ–º –Ω–∞–∑–≤–∞–Ω–∏–µ –∫–Ω–∏–≥–∏
        author: event.target.author.value.toLowerCase(), // –ü–æ–ª—É—á–∞–µ–º –∞–≤—Ç–æ—Ä–∞ –∫–Ω–∏–≥–∏
    };
    event.target.title.value = ""; // –û—á–∏—â–∞–µ–º –ø–æ–ª–µ –≤–≤–æ–¥–∞ –Ω–∞–∑–≤–∞–Ω–∏—è –∫–Ω–∏–≥–∏
    event.target.author.value = ""; // –û—á–∏—â–∞–µ–º –ø–æ–ª–µ –≤–≤–æ–¥–∞ –∞–≤—Ç–æ—Ä–∞ –∫–Ω–∏–≥–∏
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ —É–∂–µ —Ç–∞–∫–∞—è –∫–Ω–∏–≥–∞ –≤ –º–∞—Å—Å–∏–≤–µ
    const arrayCheck = books.find(
        (el) => el.title === task.title && el.author === task.author
    );
    // –ï—Å–ª–∏ –∫–Ω–∏–≥–∞ —É–∂–µ –µ—Å—Ç—å, –≤—ã–≤–æ–¥–∏–º —Å–æ–æ–±—â–µ–Ω–∏–µ –æ–± –æ—à–∏–±–∫–µ
    if (arrayCheck) {
        alert("–≠—Ç–∞ –∫–Ω–∏–≥–∞ —É–∂–µ –µ—Å—Ç—å –≤ —Å–ø–∏—Å–∫–µ üìö");
    } else {
        // –î–æ–±–∞–≤–ª—è–µ–º –∫–Ω–∏–≥—É –≤ –º–∞—Å—Å–∏–≤
        books.push(task);
        // –°–æ–∑–¥–∞–µ–º –Ω–æ–≤—ã–π —ç–ª–µ–º–µ–Ω—Ç —Å–ø–∏—Å–∫–∞
        const li = document.createElement("li");
        // –î–æ–±–∞–≤–ª—è–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ —Å–æ–±—ã—Ç–∏—è –¥–ª—è –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏—è —Å—Ç–∏–ª—è
        li.addEventListener("click", (event) => {
            if (event.target.style.textDecoration === "line-through") {
                event.target.style.textDecoration = "none";
            } else {
                event.target.style.textDecoration = "line-through";
            }
        });
        // –î–æ–±–∞–≤–ª—è–µ–º —Ç–µ–∫—Å—Ç –∫–Ω–∏–≥–∏ –≤ —ç–ª–µ–º–µ–Ω—Ç —Å–ø–∏—Å–∫–∞
        li.textContent = `–ö–Ω–∏–≥–∞: ${task.title}, –ê–≤—Ç–æ—Ä: ${task.author}`;
        // –î–æ–±–∞–≤–ª—è–µ–º —ç–ª–µ–º–µ–Ω—Ç —Å–ø–∏—Å–∫–∞ –≤ ul
        bookList.append(li);
    }
});